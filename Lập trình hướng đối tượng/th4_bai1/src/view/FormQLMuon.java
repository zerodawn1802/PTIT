    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        int mabd = Integer.parseInt(cbMaBD.getSelectedItem().toString());
        int maSach = Integer.parseInt(cbMaSach.getSelectedItem().toString());
        if(trung2Ma(mabd, maSach)){
            JOptionPane.showMessageDialog(this, "Khong duoc trung 2 ma");
            cbMaBD.requestFocus();
            return;
        }
        int sl=0;
        int sltv = getSachByMa(maSach).getSoLuong();
        try{
            sl = Integer.parseInt(jTextField3.getText());
            if(sl>sltv){
                JOptionPane.showMessageDialog(btnThemMoi, "Khong du sach");
                jTextField3.requestFocus();
                return;
            }
            String tt = jTextField4.getText();
            QLMuon ql = new QLMuon(getBDByMa(mabd),getSachByMa(maSach),sl,tt);
            tm.addRow(ql.toObject());
            lqlmuon.add(ql);
            // sua lai so luong sach o thu vien
            String fsach ="src/controller/sach.txt";
            List<Sach> listsach = IOFile.doc(fsach);
            for(Sach i:listsach){
                if(i.getMa()==maSach){
                    i.setSoLuong(sltv-sl);
                }
            }
//            getSachByMa(maSach).setSoLuong(sltv-sl);
            IOFile.viet(fsach, listsach);
       }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Chi nhap so please");
            jTextField3.requestFocus();
        }
    }//GEN-LAST:event_btnThemMoiActionPerformed

