    private void loadData(){
        File f = new File(fqlmuon);
        if(f.exists()){
            lqlmuon = IOFile.doc(fqlmuon);
        }
        else lqlmuon = new ArrayList<>();
    }
    private void loadMaSachVaMaBD(){
        cbMaBD.removeAllItems();
        cbMaSach.removeAllItems();
        String fsach ="src/controller/sach.txt";
        String fbandoc ="src/controller/bandoc.txt";
        List<BanDoc> listbd = IOFile.doc(fbandoc);
        List<Sach> listsach = IOFile.doc(fsach);
        for(BanDoc i:listbd){
            cbMaBD.addItem(i.getMa()+"");
        }
        for(Sach i:listsach){
            cbMaSach.addItem(i.getMa()+"");
        }
    }
    private boolean trung2Ma(int mabd, int maSach){
        for(QLMuon i:lqlmuon){
            if(i.getBanDoc().getMa()==mabd && i.getSach().getMa()==maSach) return true;
        }
        return false;
    }
    private int demDauSach(int mabd){
        int cnt =0;
        for(QLMuon i:lqlmuon){
            if(i.getBanDoc().getMa()==mabd) cnt++;
        }
        return cnt;
    }

