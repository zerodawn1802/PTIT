    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if(row<0||row>jTable1.getRowCount()){
            JOptionPane.showMessageDialog(this, "Chon sach de xoa");
        }
        else{
            tm.removeRow(row);
            lsach.remove(row);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if(row<0||row>jTable1.getRowCount()){
            JOptionPane.showMessageDialog(this, "Chon sach de sua");
        }
        else{
            jTextField1.setText(jTable1.getValueAt(row, 0).toString());
            jTextField2.setText(jTable1.getValueAt(row, 1).toString());
            jTextField3.setText(jTable1.getValueAt(row, 2).toString());
            jTextField4.setText(jTable1.getValueAt(row, 4).toString());
            for(int i=0;i<jComboBox1.getItemCount();i++){
                if(jTable1.getValueAt(row, 3).toString().equals(jComboBox1.getItemAt(i))){
                    jComboBox1.setSelectedIndex(i);
                    break;
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

