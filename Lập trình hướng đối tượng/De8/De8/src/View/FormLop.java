    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (lphong.size() > 0) {
            Lop.setSma(lphong.get(lphong.size() - 1).getMa()+ 1);
        } else {
            lphong = new ArrayList<>();
        }
        jTextField1.setText(Lop.getSma() + "");
        jTextField2.requestFocus();

        try {
            String tl = jTextField2.getText();
            String ssv = jTextField3.getText();
            if (tl.isEmpty() || ssv.isEmpty()) {
                throw new TrongEX();
            }
            int sosinhvien = Integer.parseInt(jTextField3.getText());
            if (sosinhvien< 0) {
                throw new SoEX();
            }
            Lop p = new Lop(Integer.parseInt(jTextField1.getText()), tl, sosinhvien);
            tm.addRow(p.toObjects());
            lphong.add(p);
        } catch (TrongEX ex) {
            JOptionPane.showMessageDialog(this, "khong bo trong");
            jTextField2.requestFocus();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "phai nhap so ghe la so");
            jTextField3.requestFocus();
        } catch (SoEX ex) {
            JOptionPane.showMessageDialog(this, "nhap so duong");
            jTextField3.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

