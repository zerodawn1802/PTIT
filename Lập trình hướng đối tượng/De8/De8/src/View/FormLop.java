    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row < 0 || row > jTable1.getRowCount()) {
            JOptionPane.showMessageDialog(this, "chon hang de sua");
        } else {
            try {
            String tl = jTextField2.getText();
            String ssv = jTextField3.getText();
            if (tl.isEmpty() || ssv.isEmpty()) {
                throw new TrongEX();
            }
            int sosinhvien = Integer.parseInt(jTextField3.getText());
            if (sosinhvien< 0) {
                throw new SoEX();
            }
            Lop p = new Lop(Integer.parseInt(jTextField1.getText()), tl, sosinhvien);
            tm.removeRow(row);
            tm.insertRow(row, p.toObjects());
            lphong.set(row, p);
        } catch (TrongEX ex) {
            JOptionPane.showMessageDialog(this, "khong bo trong");
            jTextField2.requestFocus();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "phai nhap so ghe la so");
            jTextField3.requestFocus();
        } catch (SoEX ex) {
            JOptionPane.showMessageDialog(this, "nhap so duong");
            jTextField3.requestFocus();
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

